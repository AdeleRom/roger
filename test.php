            //скрипт для квиза
            var $slider = $('#slider'),
                $next = $('.next'),
                $slide = $slider.find('.page-slide'),
                slideCount = $('.page-slide').length - 1,
                progress = '',
                eachClick = 0,
                nextClicked = 0,
                counter = 0,
                progress = $('.for-progress').outerWidth(),
                progStep = progress / slideCount,
                eachClick = (progStep / progress) * 100,
                progBar = $('.progress-bar').width(),
                progBar = progBar+eachClick,
                count = 0,
                summ = 0;

            $('.progress-bar').css({width: progBar+'%'});

            var currentSlide = 0,
                question = 1,
                allSlides = $slider.find('.page-slide').length - 1; // index start from 0

            $slider.find('.page-slide').eq(0).addClass('active');
            $('#slider .percents .slide-count').text(allSlides);


            function nextSlide() {

                var currentQuestion = question + currentSlide;


                if (currentSlide < allSlides) {

                    nextClicked = counter + currentSlide;
                    counter = nextClicked;

                    nextWidth = progBar + eachClick;
                    progBar = nextWidth;

                    //console.log(eachClick);

                    $('.progress-bar').css({
                        width: progBar >= progress ? 100 + '%' : progBar + '%'
                    });

                    $('.perc').text(progBar >= progress ? 100 + '%' : Math.round(progBar) + '%');

                    $('.page-slide').removeClass('active');

                    $slide.eq(currentSlide + 1).addClass('active');

                    currentSlide += 1;

                    if ($('.page-slide').eq(-1).hasClass('active')) {
                        $('.for-progress').addClass('d-none');
                        if (summ > 0 && summ <= 4) {
                            $('.page-slide.result .rst-1').removeClass('d-none');
                        }
                        else if(summ > 4 && summ <= 7) {
                            $('.page-slide.result .rst-2').removeClass('d-none');
                        }
                        else {
                            $('.page-slide.result .rst-3').removeClass('d-none');
                        }
                    }

                    if (currentQuestion < allSlides) {
                        $('#slider .percents .current-count').text(question + currentSlide);
                    }


                }
            }

            $next.on('click', function(e) {
                
                var score = $(this).data('score');
                    
                
                count = summ+score;
                summ = count;
                
                console.log(summ);
                
                e.preventDefault();

                nextSlide($(this));
            });

